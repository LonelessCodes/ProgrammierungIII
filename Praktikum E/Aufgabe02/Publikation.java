package Aufgabe02;

public class Publikation {
    public String titel;
    public String autor;
    public String verlag;
    public int jahr;

    public Publikation(String titel, String autor, String verlag, int jahr) {
        this.titel = titel;
        this.autor = autor;
        this.verlag = verlag;
        this.jahr = jahr;
    }
}
